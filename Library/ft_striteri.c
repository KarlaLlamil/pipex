#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	int	i;
	int	n;

	i = 0;
	n = ft_strlen(s);
	while (i < n)
	{
		f(i, &s[i]);
		++i;
	}
}
